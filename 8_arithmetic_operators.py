# Arithmetic Operators: +, -, *, /, %, **, // (exponentiation).

x = 10
y = 3


sum_result = x + y          # Addition
subtraction_result = x - y # Subtraction
product_result = x * y      # Multiplication
power_result = x ** y       # Exponentiation
division_result = x / y        # Division
remainder_result = x % y        # Remainder (Division)
floor_division_result = x // y # Floor division

print(sum_result,subtraction_result, product_result, power_result, division_result, remainder_result, floor_division_result)

