x = int(10)  # x will be 10
y = int(9.2)  # y will be 9
z = int("7")  # z will be 7

print(x, y, z)

a = float(10)  # a will be 10.0
b = float(9.2)  # b will be 9.2
c = float("7")  # c will be 7.0
d = float("7.2")  # d will be 7.2

print(a, b, c, d)

i = str("a7")  # i will be 's1'
j = str(10)  # j will be '2'
k = str(7.0)  # k will be '7.0'

print(i, j, k)

