# Comparison Operators: ==, !=, >, <, >=, <=

x, y = 10, 3

a, b = 4, 4

# print(a == b) # equal? True

c, d = "John", "Jane" # only equality and non-equality works with strings

print(c == d) # equal? True

print (x == y) #not equal? False

print (x != y) # not equal? True

print (a != b) # not equal? False

print (x > y) #True

print (a > b) #False

print (x < y) #False

print (a < b) #False

print (x <= y) # False

print (a <= b) # True

print (x >= y) # True

print (a >= b) # True

